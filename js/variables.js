/*
Kintamieji:
(Inicijavimo budas)
const - default budas
let - antrinis budas, kai zinome, jog reiksme keisis
var - pats seniausias, geriau nenaudoti, jei rupi saugumas

Duomenu tipoas: 
number = 1; 1.5; 9999;
string (tekstas) = abd; Lukas;
boolean (logine reiksme) = true; false.
array (sarasas, masyvas, matrica)

*/

